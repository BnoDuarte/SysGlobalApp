    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
        String id_produto = ""+jTableProdutos.getValueAt(jTableProdutos.getSelectedRow(), 0);
        
        connProdutos.conexao();
        
        try {
            connProdutos.executaSQL("SELECT * FROM produtos WHERE id = '"+id_produto+"'");
            connProdutos.rs.first();
            
            jTextCodigo.setText(String.valueOf(connProdutos.rs.getInt("id")));
            jTextNome.setText(connProdutos.rs.getString("nome"));
            jTextAreaDescricao.setText(connProdutos.rs.getString("descricao"));
            jCBoxCategoria.setSelectedItem(connProdutos.rs.getString("categoria"));
            jCBoxFabricante.setSelectedItem(connProdutos.rs.getString("fabricante"));
            jCBoxRede.setSelectedItem(connProdutos.rs.getString("rede"));
            jCBoxSistemaOperacional.setSelectedItem(connProdutos.rs.getString("sistema_operacional"));
            jCBoxCor.setSelectedItem(connProdutos.rs.getString("cor"));
            jTextQteMin.setText(connProdutos.rs.getString("qte_min"));
            jFormattedValor.setText(connProdutos.rs.getString("valor"));
            jCBoxStatus.setSelectedItem(connProdutos.rs.getString("status"));
            
            connProdutos.desconecta();
            
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnPesquisaAvancada.setEnabled(false);
            btnPesquisarCodigo.setEnabled(false);
            
            jTextCodigo.setEnabled(!true);
            jTextNome.setEnabled(true);
            jCBoxCategoria.setEnabled(true);
            jCBoxFabricante.setEnabled(true);
            jCBoxRede.setEnabled(true);
            jCBoxSistemaOperacional.setEnabled(true);
            jCBoxCor.setEnabled(true);
            jTextQteMin.setEnabled(true);
            jFormattedValor.setEnabled(true);
            jCBoxStatus.setEnabled(true);
            jTextAreaDescricao.setEnabled(true);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar os dados.\nERRO: "+ex);
        }
        
        
    }//GEN-LAST:event_jTableProdutosMouseClicked

